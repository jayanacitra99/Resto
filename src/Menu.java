    private void selectData() {
        String kolom[] = {"Nama Makanan","Nama Minuman","Harga Makanan","Harga Minuman"};
        DefaultTableModel dtm = new DefaultTableModel (null, kolom);
        String SQL = "SELECT * FROM tb_makanan";
        ResultSet rs = KoneksiDB.executeQuery(SQL);
        try {
            while(rs.next()){
                String NamaMakanan = rs.getString(1);
                String NamaMinuman = rs.getString(2);
                String HargaMakanan = rs.getString(3);
                String HargaMinuman = rs.getString(4);
                String data[] = {NamaMakanan,NamaMinuman,HargaMakanan,HargaMinuman};
                dtm.addRow(data);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Menu.class.getName()).log(Level.SEVERE, null, ex);
        }
        Table1.setModel(dtm);
    }
